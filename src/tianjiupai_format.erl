-module(tianjiupai_format).

%%====================================================================================================
%% Exported API
%%====================================================================================================
-export([
    encode_failure_response/1
]).

%%====================================================================================================
%% Exported Functions
%%====================================================================================================
-spec encode_failure_response(Reason :: term()) -> binary().
encode_failure_response(Reason) ->
    erlang:list_to_binary(lists:flatten(io_lib:format("~w", [Reason]))).
